import turtle
t = turtle.Turtle()

#setHeading


t.setheading(90) #that means turtle ti uporer dike muth kore thakbe
t.setheading(180) #turtle ti bam dike mukh kore thakbe
t.setheading(270) #turtle ti nicher dike mukh kore thakbe
t.setheading(360) #turtle ti dan e mukh kore thakbe

#forward


t.forward(100) #dan er dike daag kore nibe "forward & fd" same
t.fd(-200) #picher dike jabe

#backward

t.backward(200) #pichone dag katar jonno
t.bk(-500) #eta dile samne dike jabe ( biporit )

#left

t.left(45) #it goes left 45 deg
t.forward(100)

#right

t.right(75)
t.forward(100)